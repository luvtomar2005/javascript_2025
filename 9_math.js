// Now we learn about Math function in javascript
// Math.round -> Round to the nearest integer.. 
console.log(Math.round(4.5));

// Math.floor() -> Always round down to the nearest integer
console.log(Math.floor(4.9));

// Math.ceil() -> Always round up to the nearest integer
console.log(Math.ceil(4.2));


//Math.abs -> basically the number is how much far from 0
console.log(Math.abs(4.434342));
console.log(Math.abs(-2));

// Math.trun -> Returns the integer part of x
console.log(Math.trunc(93.32));

// Math.pow -> Return the power of x to y
console.log(Math.pow(2,4));

console.log(Math.round(Math.random() * 100));
